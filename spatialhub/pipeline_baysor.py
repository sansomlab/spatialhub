"""==================
Pipeline baysor.py
==================

Overview
--------

This pipeline runs baysor, using a transcripts file annotated with a prior cell mask
(currently only supporting the use of AtoMx default mask as formatted when running spatialhub ashlar;
but the pre-processing step could be skipped and performed manually if starting from another prior)


Configuration
-------------

The pipeline requires a :file:`pipeline_baysor.yml` file.

An example configuration file can be generated by executing:

spatialhub baysor config


Inputs
------

(stitched) image


Requirements
------------

The following software is required:

#. python
#. baysor

# WARNING: baysor/0.7.1 (and earlier) is incompatible with some of the default Sansom Lab modules
# => *before* running this pipeline, but *after* activating your spatial_cgat virtual environment,
# update ~/.bashrc to load only the appropriate version of python and baysor


Output files
------------

The pipeline generates the following outputs:

#. a segmentation mask (compatible with spatialdata)
#. a matching transcripts coordinates file with cell compartment assignment [work in progress]
#. a matching cellxgene count matrix [work in progress]


Code
====

"""

