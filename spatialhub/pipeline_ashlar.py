#@active_if(runAshlar)
def split_jobs():

    if not os.path.exists("ashlar.dir"):
        os.mkdir("ashlar.dir")
    
    if not os.path.exists("ashlar.dir/logs"):
        os.mkdir("ashlar.dir/logs")

    for slide in S.slide_ids():
        sentinel_file = os.path.join("ashlar.dir/logs", slide + "_splitFlatFiles.sentinel")
        yield([None, sentinel_file])


@files(split_jobs)
def splitFlatFiles(infile, outfile):
    '''
    Splits large CosMx flat files by FOV (significantly lowers downstream memory requirements)
    '''

    input_slide = os.path.basename(outfile)[:-len("_splitFlatFiles.sentinel")]

    t = T.setup(infile, outfile, PARAMS,
                memory=PARAMS["ashlar_low_mem"],
                cpu=1)
    
    #pattern = "XYZ"  # if wanting to keep original name
    
    # WARNING: cosmx_setup.sh does not work if splitExprMat/splitTxFile directories are not empty.
    # => Add option to check they are empty, or trigger error if directory already exists (?)
    statement = '''%(spatialhub_code_dir)s/bash/cosmx_setup.sh 
                   --directory=%(projDir)s
                   --slide=%(input_slide)s
                   &> %(log_file)s
                ''' % dict(PARAMS, **t.var, **locals())
    
    P.run(statement, **t.resources)
    IOTools.touch_file(outfile)



# The follow-up tasks need to run at the sample-level.

def sample_jobs():

    if not os.path.exists("ashlar.dir"):
        os.mkdir("ashlar.dir")
    
    if not os.path.exists("ashlar.dir/logs"):
        os.mkdir("ashlar.dir/logs")

    for sample in S.sample_ids():
        sentinel_file = os.path.join("ashlar.dir/logs", sample + "_ashlarSetup.sentinel")
        yield([None, sentinel_file])


@files(sample_jobs)
def ashlarSetup(infile, outfile):
    '''
    Creates symlinks to original FOV files, grouped within one separate sub-directory per sample (per slide)
    '''

    input_sample = os.path.basename(outfile)[:-len("_ashlarSetup.sentinel")]

    t = T.setup(infile, outfile, PARAMS,
                memory=PARAMS["ashlar_low_mem"],
                cpu=1)
    
    statement = '''python %(spatialhub_code_dir)s/python/ashlar_setup.py 
                   --projDir=%(projDir)s
                   --sampleKey=%(input_sample)s
                   --fov2sample=%(sample_table)s
                   &> %(log_file)s
                ''' % dict(PARAMS, **t.var, **locals())
    
    P.run(statement, **t.resources)
    IOTools.touch_file(outfile)


@transform(ashlarSetup,
           regex(r"(.*)/(.*)_ashlarSetup.sentinel"),
           r"\1/\2_ashlarStitch.sentinel")
def ashlarStitch(infile, outfile):
    '''
    Stitches FOV files from each sample into one whole microscopy image
    '''

    t = T.setup(infile, outfile, PARAMS,
                memory=PARAMS["ashlar_med_mem"],
                cpu=1)
    
    input_sample = os.path.basename(infile)[:-len("_ashlarSetup.sentinel")]
    px_size = PARAMS["ashlar_pixel_size"]
    tile_ovlp = PARAMS["ashlar_tile_overlap"]
    save_lite_img = PARAMS["ashlar_save_three_color_img"]
    keep_channels = PARAMS["ashlar_keep_channels"]
    
    statement = '''python %(spatialhub_code_dir)s/python/ashlar_stitch_cosmx.py 
                   --sampleKey=%(input_sample)s
                   --fov2sample=%(sample_table)s
                   --pxSize=%(px_size)s
                   --tileOvlp=%(tile_ovlp)s
                   --saveLiteImg=%(save_lite_img)s
                   --keepChannels=%(keep_channels)s
                   &> %(log_file)s
                ''' % dict(PARAMS, **t.var, **locals())
    
    P.run(statement, **t.resources)
    IOTools.touch_file(outfile)


@follows(splitFlatFiles)
@transform(ashlarStitch,
           regex(r"(.*)/(.*)_ashlarStitch.sentinel"),
           r"\1/\2_ashlarConvertCoords.sentinel")
def ashlarConvertCoords(infile, outfile):
    '''
    Convert original transcripts coordinates into new coordinate system for each sample
    '''

    t = T.setup(infile, outfile, PARAMS,
                memory=PARAMS["ashlar_high_mem"],
                cpu=1)
    
    input_sample = os.path.basename(infile)[:-len("_ashlarStitch.sentinel")]
    px_size = PARAMS["ashlar_pixel_size"]
    
    statement = '''Rscript %(spatialhub_code_dir)s/R/ashlar_convert_coords.R
                   --projDir=%(projDir)s
                   --sampleKey=%(input_sample)s
                   --fov2sample=%(sample_table)s
                   --pxSize=%(px_size)s
                   &> %(log_file)s
                ''' % dict(PARAMS, **t.var, **locals())
    
    P.run(statement, **t.resources)
    IOTools.touch_file(outfile)


@active_if(PARAMS['create_zarr'])
@transform(ashlarConvertCoords,
           regex(r"(.*)/(.*)_ashlarConvertCoords.sentinel"),
           r"\1/\2_ashlarCreateZarr.sentinel")
def ashlarCreateZarr(infile, outfile):
    '''
    Create a zarr (spatial data) object to facilitate visualization of Ashlar output
    '''

    if not os.path.exists("zarr.dir"):
        os.mkdir("zarr.dir")

    t = T.setup(infile, outfile, PARAMS,
                memory=PARAMS["sdata_mem"],
                cpu=1)
    
    input_sample = os.path.basename(infile)[:-len("_ashlarConvertCoords.sentinel")]
    
    statement = '''python %(spatialhub_code_dir)s/python/ashlar_zarr.py
                   --projDir=%(projDir)s
                   --sampleKey=%(input_sample)s
                   --fov2sample=%(sample_table)s
                   --channels=%(channels_dict)s
                   --rmProbes=%(remove_probes)s
                   --probeKey=%(probe_key)s
                   --x=%(x_coord)s
                   --y=%(y_coord)s
                   --importAtomxMask=%(import_atomx_mask)s
                   &> %(log_file)s
                ''' % dict(PARAMS, **t.var, **locals())
    
    P.run(statement, **t.resources)
    IOTools.touch_file(outfile)



