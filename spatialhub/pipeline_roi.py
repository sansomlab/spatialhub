# ----------------------------- Pipeline tasks ----------------------------- #

def sample_jobs():

    if not os.path.exists("roi.dir"):
        os.mkdir("roi.dir")
    
    if not os.path.exists("roi.dir/logs"):
        os.mkdir("roi.dir/logs")
    
    if not os.path.exists("roi.dir/" + PARAMS['segmentation_mask']):
        os.mkdir("roi.dir/" + PARAMS['segmentation_mask'])
    
    for sample in S.sample_ids():
        sentinel_file = os.path.join("roi.dir/logs", sample + "_" + PARAMS['segmentation_mask'] + "_roiFetch.sentinel")
        yield([None, sentinel_file])


@files(sample_jobs)
#@active_if(PARAMS['preprocessing'])
def roiFetch(infile, outfile):
    '''
    Fetch cell IDs within a ROI and for a given segementation mask
    '''

    t = T.setup(infile, outfile, PARAMS,
                memory=PARAMS["mem"],
                cpu=1)
    
    input_sample = os.path.basename(outfile)[:-len("_" + PARAMS['segmentation_mask'] + "_roiFetch.sentinel")]

    statement = '''python %(spatialhub_code_dir)s/python/roi_fetch.py
                   --sampleKey=%(input_sample)s
                   --fov2sample=%(sample_table)s
                   --segMask=%(segmentation_mask)s
                   --ROI=%(roi_name)s
                   &> %(log_file)s
                ''' % dict(PARAMS, **t.var, **locals())
    
    P.run(statement, **t.resources)
    IOTools.touch_file(outfile)




